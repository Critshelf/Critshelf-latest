function generateCsv() {
  // Deduplicate games by BGG ID to prevent Firestore collisions
  const uniqueGamesMap = new Map();
  games.forEach(g => uniqueGamesMap.set(g.id, g));
  const uniqueGames = Array.from(uniqueGamesMap.values());

  const header = "boardgame,url\n";
  const rows = uniqueGames.map(g => {
    const escapedName = g.name.includes(',') ? `"${g.name}"` : g.name;
    const url = `https://boardgamegeek.com/boardgame/${g.id}/${g.name.toLowerCase().replace(/[^a-z0-9]/g, '-')}`;
    return `${escapedName},${url}`;
  });

  const csvContent = header + rows.join('\n');
  fs.writeFileSync(path.join(process.cwd(), 'games.csv'), csvContent);
  console.log(`Successfully generated games.csv with ${uniqueGames.length} unique games.`);
}

